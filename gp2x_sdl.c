void 
gp2x_sdl_draw_rectangle(int x, int y, int w, int h, int border, int mode) 
{
  ushort *vram = (ushort *)gp2x_sdl_get_vram_addr(x, y);
  int xo, yo;
  if (mode == GP2X_SDL_XOR) {
    for (xo = 0; xo < w; xo++) {
      vram[xo] ^=  border;
      vram[xo + h * GP2X_LINE_SIZE] ^=  border;
    }
    for (yo = 0; yo < h; yo++) {
      vram[yo * GP2X_LINE_SIZE] ^=  border;
      vram[w + yo * GP2X_LINE_SIZE] ^=  border;
    }
  } else {
    for (xo = 0; xo < w; xo++) {
      vram[xo] =  border;
      vram[xo + h * GP2X_LINE_SIZE] =  border;
    }
    for (yo = 0; yo < h; yo++) {
      vram[yo * GP2X_LINE_SIZE] =  border;
      vram[w + yo * GP2X_LINE_SIZE] =  border;
    }
  }
}

void 
gp2x_sdl_fill_rectangle(int x, int y, int w, int h, int color, int mode)
{
  ushort *vram  = (ushort *)gp2x_sdl_get_vram_addr(x, y);
  int xo, yo;
  if (mode == GP2X_SDL_XOR) {
    for (xo = 0; xo <= w; xo++) {
      for (yo = 0; yo <= h; yo++) {
        if ( ((xo == 0) && ((yo == 0) || (yo == h))) ||
             ((xo == w) && ((yo == 0) || (yo == h))) ) {
          /* Skip corner */
        } else {
          vram[xo + yo * GP2X_LINE_SIZE] ^=  color;
        }
      }
    }
  } else {
    for (xo = 0; xo <= w; xo++) {
      for (yo = 0; yo <= h; yo++) {
        vram[xo + yo * GP2X_LINE_SIZE] =  color;
      }
    }
  }
}

void 
gp2x_sdl_put_char(int x, int y, int color, int bgcolor, char c, int drawfg, int drawbg)
{
  int cx;
  int cy;
  int b;
  int index;

  ushort *vram = (ushort *)gp2x_sdl_get_vram_addr(x, y);
  index = ((ushort)c) * 8;

  for (cy=0; cy<8; cy++) {
    b=0x80;
    for (cx=0; cx<8; cx++) {
      if (gp2x_font[index] & b) {
        if (drawfg) vram[cx + cy * 320] = color;
      } else {
        if (drawbg) vram[cx + cy * 320] = bgcolor;
      }
      b = b >> 1;
    }
    index++;
  }
}

