int
gp2x_sdl_init(void)
{
  if (SDL_Init(SDL_INIT_VIDEO | SDL_INIT_AUDIO | SDL_INIT_JOYSTICK | SDL_INIT_TIMER) < 0) {
     return 0;
  }

  //if (SDL_InitSubSystem(SDL_INIT_VIDEO) < 0) {
  //  return 0;
  //}
  back_surface=SDL_SetVideoMode(320, 240, 16, SDL_HWSURFACE | SDL_HWPALETTE | SDL_DOUBLEBUF);

  if ( !back_surface) {
    return 0;
  }

  SDL_ShowCursor(SDL_DISABLE);

  joystick=SDL_JoystickOpen(0);

  SDL_FillRect(back_surface,NULL,SDL_MapRGB(back_surface->format,0x0,0x0,0x0));
  SDL_FillRect(back_surface,NULL,SDL_MapRGB(back_surface->format,0x0,0x0,0x0));
  SDL_Flip(back_surface);

  SDL_Surface* img_temp;

  if(!(img_temp=IMG_Load("./graphics/intellivision.png")))
      fprintf(stderr,"Error loading pad: \n",SDL_GetError());
  if(img_temp) {
	KeyPad=SDL_DisplayFormatAlpha(img_temp);
	SDL_FreeSurface(img_temp);
  }

  SDL_Delay(1000);        // Give sound some time to init

  SDL_PauseAudio(0);
}

void
gp2x_sdl_exit(int status)
{
  fprintf(stdout,"sdl_exit\n");
  if(KeyPad) SDL_FreeSurface(KeyPad);
  if(back_surface) SDL_FreeSurface(back_surface);
  if(back2_surface) SDL_FreeSurface(back2_surface);
  SDL_CloseAudio();
  SDL_Quit();

  fprintf(stdout,"sdl_exit->exit(%i)\n",status);
  exit(status);
}
